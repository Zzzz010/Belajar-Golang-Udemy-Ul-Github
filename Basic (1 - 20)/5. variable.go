package main

import "fmt"

func main() {

	// Shortcut No 2

	var (
		myname  = "Raung Kawijayan"
		trainname = "Matarmaja"
	)

	fmt.Println(myname)
	fmt.Println(trainname)

	// default

	var namaku string

	namaku = "Raung"
	fmt.Println(namaku)

	namaku = "Raung K"
	fmt.Println(namaku)

	// Shortcut No 1

	var namaS = "Argo Wilis"
	fmt.Println(namaS)

	// Declare variable int8

	var age int8 = 21
	fmt.Println(age)

	// Declare without var ( instead using := )

	selatan3 := "Bima"
	fmt.Println(selatan3)

	backname = "Kawijayan"
	fmt.Println(backname)

}
